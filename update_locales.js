for (const lang of langs) {
  const filePath = path.join(localesDir, `${lang}.ts`);
  let content = fs.readFileSync(filePath, 'utf8');
  
  // Find the last "}" which closes the object, but before "export default"
  const lastBraceIndex = content.lastIndexOf('}');
  
  if (lastBraceIndex === -1) {
    console.error(`Could not find closing brace in ${lang}.ts`);
    continue;
  }
  
  let newKeysStr = '\n';
  const data = translations[lang];
  for (const [key, value] of Object.entries(data)) {
    // Only append if it's not already in the file (just in case)
    if (!content.includes(`'${key}'`)) {
      newKeysStr += `  '${key}': ${JSON.stringify(value)},\n`;
    }
  }
  
  const newContent = content.slice(0, lastBraceIndex) + newKeysStr + content.slice(lastBraceIndex);
  fs.writeFileSync(filePath, newContent, 'utf8');
  console.log(`Updated ${lang}.ts`);
}
